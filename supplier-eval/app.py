@app.post("/api/performance/parse-file")
def parse_performance_file():
    file = request.files.get("file")
    if not file:
        return jsonify({"error": "파일이 없습니다"}), 400

    filename = file.filename.lower()
    rows = []

    if filename.endswith(".csv"):
        content = file.read().decode("utf-8-sig")
        reader = csv.DictReader(io.StringIO(content))
        rows = list(reader)
    elif filename.endswith(".xlsx") or filename.endswith(".xls"):
        import openpyxl
        wb = openpyxl.load_workbook(io.BytesIO(file.read()))
        ws = wb.active
        headers = [str(c.value).strip() if c.value else "" for c in ws[1]]
        for row in ws.iter_rows(min_row=2, values_only=True):
            if any(v is not None for v in row):
                rows.append(dict(zip(headers, [str(v).strip() if v is not None else "" for v in row])))
    else:
        return jsonify({"error": "CSV 또는 Excel(.xlsx) 파일만 지원합니다"}), 400

    if not rows:
        return jsonify({"error": "데이터가 없습니다"}), 400

    def gv(row, *keys):
        for k in keys:
            if k in row and row[k] not in (None, ""):
                return str(row[k]).strip()
        return ""

    row = rows[0]
    iso_raw = gv(row, "ISO 보유 여부", "iso_held", "ISO보유여부")
    iso_held = iso_raw.lower() not in ("", "미보유", "false", "0", "n", "no")

    def fv(row, default, *keys):
        v = gv(row, *keys)
        try:
            return float(v) if v else default
        except ValueError:
            return default

    result = {
        "isoHeld": iso_held,
        "isoExpiry": gv(row, "ISO 유효기간", "iso_expiry", "ISO유효기간"),
        "deliveryTarget": fv(row, 100, "납기율 목표(%)", "delivery_target", "납기율목표"),
        "deliveryActual": fv(row, 0, "납기율 실적(%)", "delivery_actual", "납기율실적"),
        "defectTarget": fv(row, 0, "부적합율 목표(%)", "defect_target", "부적합율목표"),
        "defectActual": fv(row, 0, "부적합율 실적(%)", "defect_actual", "부적합율실적"),
        "evaluator": gv(row, "평가자", "evaluator"),
        "reviewer": gv(row, "검토자", "reviewer"),
        "approver": gv(row, "승인자", "approver"),
        "selfAssessment": gv(row, "Self Assessment", "self_assessment") or "해당사항 없음",
        "supplyChain": gv(row, "Supply Chain 검증", "supply_chain") or "해당사항 없음",
        "productEval": gv(row, "제품 평가", "product_eval") or "제품 검사서 참조",
    }
    return jsonify(result)


@app.get("/api/performance/template")
def download_template():
    import openpyxl
    from openpyxl.styles import Font, PatternFill, Alignment

    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "실적입력 템플릿"
    headers = [
        "ISO 보유 여부", "ISO 유효기간", "납기율 목표(%)", "납기율 실적(%)",
        "부적합율 목표(%)", "부적합율 실적(%)", "평가자", "검토자", "승인자",
        "Self Assessment", "Supply Chain 검증", "제품 평가",
    ]
    ws.append(headers)
    ws.append(["보유", "2028-12-31", 100, 99, 0, 0, "", "", "",
               "해당사항 없음", "해당사항 없음", "제품 검사서 참조"])
    for cell in ws[1]:
        cell.font = Font(bold=True)
        cell.fill = PatternFill(fill_type="solid", fgColor="D9E1F2")
        cell.alignment = Alignment(horizontal="center")
    for col in ws.columns:
        ws.column_dimensions[col[0].column_letter].width = 18
    buf = io.BytesIO()
    wb.save(buf)
    buf.seek(0)
    return send_file(
        buf,
        mimetype="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
        as_attachment=True,
        download_name="실적입력_템플릿.xlsx",
    )


